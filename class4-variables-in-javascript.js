//Although JavaScript has different available variable types, it is not a strongly
//typed language such as C/C++, C#, and Java.
// You dont have to specify what type of variable is
//To declare, use var

//Variables in JavaScript: Number, String, Boolean, Function, Object, Undefined, 
//Null, Array and Regular Expression 

// Integer Literals (Number)

var num = 1;

console.log(num);
console.log(typeof(num)); //number

// Floating-Point Literals (Number)

var price = 1.5;

console.log(price);
console.log(typeof(price)); //number

// Date (Object)

var now = new Date();

console.log(now); //Date
console.log(typeof(now)); //Object

// Text (string)

var myName = 'Packt';

console.log(myName);
console.log(typeof(myName)); //String

// Boolean

var trueValue = true;

console.log(trueValue);
console.log(typeof(trueValue)); //Boolean

//Null

var nullVar = null; 

console.log(nullVar);
console.log(typeof(nullVar)); //Object

//Undefined

var und;

console.log(und);
console.log(typeof(und)); // Undefined